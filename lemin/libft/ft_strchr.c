#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (ft_strlen(s) == 0 && c != '\0')
	{
		if (c == 0)
			return ("");
		return (0);
	}
	return (ft_memchr((void *)s, c, ft_strlen((char *)s) + 1));
}
