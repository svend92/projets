#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	unsigned long i;
	unsigned long j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	i = 0;
	return (dest);
}
