#include "libft.h"

int	ft_intlen(int nb)
{
	int len;

	len = 0;
	if (!nb)
		return (1);
	if (nb < 0)
		nb = -nb;
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
